/**
 * Create an SVG icon element.
 * @param {string} name - Icon name from the PATHS registry.
 * @param {number} [size=16] - Width and height in pixels.
 * @returns {HTMLSpanElement} A span wrapping the SVG.
 */
export function icon(name, size = 16) {
  const paths = PATHS[name];
  if (!paths) return document.createTextNode(name);

  const span = document.createElement('span');
  span.className = 'icon';
  span.innerHTML = `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${paths}</svg>`;
  return span;
}
